    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

     try
     {
         String serverName=jTextField1.getText();
         String portNo=jTextField2.getText();
         String uniqueId=jTextField3.getText();
         String userId=jTextField4.getText();
         char ch[]=jPasswordField1.getPassword();
         String password=new String(ch);
         
         Registry reg=LocateRegistry.getRegistry(serverName,Integer.parseInt(portNo));
         LoginInterface li=(LoginInterface)reg.lookup(uniqueId);
         boolean result=li.check(userId, password);
         
         if(result)
         {
             JOptionPane.showMessageDialog(this,"Successfully logged in into RMI Application");
         }
         
         else
         {
             dispose();
         }  
         
     }catch(Exception ex){
            ex.printStackTrace();
            dispose();
     }
        
    }//GEN-LAST:event_jButton1ActionPerformed

